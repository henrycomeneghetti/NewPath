    private void btnModificarNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarNotaActionPerformed
        // TODO add your handling code here:
       
         if (tblNotas.getSelectedRowCount() == 1){
            int row = tblNotas.getSelectedRow();
            int id = Integer.parseInt (
                    tblNotas.getValueAt(row, 0).toString());
                new TelaAdicionaNota(id).setVisible(true);
               
        }
        else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha!");
        }
        
        
    }//GEN-LAST:event_btnModificarNotaActionPerformed

    private void btnExcluirNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirNotaActionPerformed
        // TODO add your handling code here:
            if (tblNotas.getSelectedRowCount() == 1) {
            Integer row = tblNotas.getSelectedRow();
            Integer id = Integer.valueOf(
                    tblNotas.getValueAt(row, 0).toString());
            Integer resposta = JOptionPane
                        .showConfirmDialog(null,
                                "Deseja realmente excluir?");
            if (resposta == JOptionPane.YES_OPTION) {
                NotasDao notasDao = new NotasDao();
                notasDao.removeNota(id);
                JOptionPane.showMessageDialog(null,
                        "Nota removida com sucesso!");
                DefaultTableModel model = 
                        (DefaultTableModel) tblNotas.getModel();
                model.removeRow(row);
            }
        }
        else {
            JOptionPane.showMessageDialog(null,
                    "Por favor, selecione uma linha!");
        }
    }//GEN-LAST:event_btnExcluirNotaActionPerformed

