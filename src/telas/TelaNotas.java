    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        NotasDao notasDao = new NotasDao();
        List<Notas> lista = notasDao.buscarNotasPorUsuario(loginUsuario);
        if(lista.isEmpty()){
            lblListaVazia.setVisible(true);
            lblListaVazia.setText("Nenhuma nota encontrada, adicione uma nova nota!");
        }
        else {
            lblListaVazia.setVisible(false);
            lblListaVazia.setText("Aqui estão suas notas! Adicione outras ou edite as existentes!");
            for (Notas notas: lista) {
            DefaultTableModel model = (DefaultTableModel) tblNotas.getModel();
            model.addRow(new Object[] {
            notas.getId(),
            notas.getTitulo(),
            notas.getDescricao(),
            notas.getDataRegistro(),
             });
        }
        }
        
    }//GEN-LAST:event_formWindowOpened

