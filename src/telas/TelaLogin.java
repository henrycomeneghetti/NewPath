    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
       String login;
        String senha;
        login = txtLogin.getText();
        senha = new String(txtSenha.getPassword());
        
        
      
        if (!login.equals("") && !senha.equals("")) {
            ContatoDao contatoDao = new ContatoDao();
 
            if (contatoDao.login(login, senha)){
                JOptionPane.showMessageDialog(null, "Login com sucesso!");
                TelaPrincipal telaPrincipal = new TelaPrincipal(login);
                
                
                
                telaPrincipal.setVisible(true);
                this.dispose();
        }
            else {
                if (login.equals("admin") && senha.equals("1234")){
                JOptionPane.showMessageDialog(null, "Login com Sucesso!");
                TelaUsuarios tela = new TelaUsuarios();
                tela.setVisible(true);
                this.dispose();

                    
                }
                else{
                   if (!contatoDao.login(login, senha)){
                       JOptionPane.showMessageDialog(null, "Login inv√°lido!");
                   }
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Favor preencher os campos!");
        }
                
    }//GEN-LAST:event_btnLogarActionPerformed

