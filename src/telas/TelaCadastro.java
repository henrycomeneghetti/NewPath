    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String nome;
        String sobrenome;
        String telefone;
        String email;
        String data;
        String login, senha, confirme;
        boolean valida = false;
        String msg = "";
        String vicio;
        nome = txtNome.getText();
        sobrenome = txtSobrenome.getText();
        telefone = txtTelefone.getText();
        email = txtEmail.getText();
        data = txtDataNascimento.getText();
        login = txtLogin.getText();
        senha = new String(txtSenha.getPassword());
        confirme = new String(txtConfirme.getPassword());
        vicio = txtVicio.getText();
        LocalDate data2 = LocalDate.now();
        Date dataAtual;

        try {
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            String dataStr = data2.format(DateTimeFormatter.ofPattern("dd/MM/yyyy")); 
            dataAtual = new Date(format.parse(dataStr).getTime());
            } 
        
        catch (ParseException e) {
        throw new RuntimeException(e);
        }
        
        if (nome.equals("")){
            valida = true;
            msg += "\nNome";
        }
        
        if (sobrenome.equals("")){
            valida = true;
            msg+= "\nSobrenome";
        }
        
        if (telefone.equals("")){
            valida = true;
            msg += "\nTelefone";
        }
        if (email.equals("")){
            valida = true;
            msg += "\nEmail";
        }
        
        if (login.equals("")) {
            valida = true;
            msg += "\nLogin";
        }
        
        if (senha.equals("")){
            valida = true;
            msg += "\nSenha";
        }
        
        if (confirme.equals("")){
            valida = true;
            msg += "\nConfirme";
        }
        
        if (!confirme.equals(senha)){
            valida = true;
            msg += "\nSenha estÃ¡ diferente de Confirme!";
        }
        if (vicio.equals("")){
            valida = true;
            msg += "\nVÃ­cio";
        }
     
        Date dataNascimento;
        
        try {
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            dataNascimento = new Date (format.parse(data).getTime());
            
        }
        catch (ParseException e) {
            throw new RuntimeException(e);
        }
        
        
        if (valida == true){
            JOptionPane.showMessageDialog(null, "Existem campos para preencher:" + msg);
        }
        else{
            Contato contato = new Contato();
            contato.setNome(nome);
            contato.setSobrenome(sobrenome);
            contato.setTelefone(telefone);
            contato.setEmail(email);
            contato.setDataNascimento(dataNascimento);
            contato.setLogin(login);
            contato.setSenha(senha);
            contato.setVicio(vicio);
            contato.setDataAtual(dataAtual);
            ContatoDao contatoDao = new ContatoDao();
            if (this.id == 0){
                contatoDao.adicionaContato(contato);
                
                JOptionPane.showMessageDialog(null, "Gravado com sucesso!");
                this.dispose();
                
            }
            if (this.id > 0){
                contato.setId(this.id);
                
                contatoDao.atualizaContato(contato);
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
                
                this.dispose();
                TelaUsuarios telaUsuarios = new TelaUsuarios();
                telaUsuarios.setVisible(true);
                
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

